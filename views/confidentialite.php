<div class="text-justify mt-3 col-lg-12">
	<strong>Quelle protection des données ?</strong>
	<p>
		Souhaitant que nos membres et visiteurs se sentent en confiance sur le site de Cnam Location et la réservation en ligne, nous nous engageons à protéger les données que nous recueillons. Nous conserverons vos données personnelles aussi longtemps que requis en vertu de la loi française pour les fins pertinentes décrites dans la présente Politique de confidentialité. Cnam Location utilise des mesures de sécurité physiques, techniques, et organisationnelles appropriées pour empêcher le traitement non autorisé ou illicite, la perte accidentelle, la destruction ou l’endommagement de vos données personnelles.
		<br>
		Notre système est encrypté. Lorsque vous envoyez par Internet des informations personnelles à locationsvoiture.fr, vos données sont protégées grâce au système de transferts sécurisés de la technologie Secure Socket Layer (SSL). Toutes les transactions faites sur notre site avec votre carte de crédit sont effectuées sur un serveur sécurisé, qui encrypte toutes les informations et offre de nombreux systèmes de protection.
	</p>
</div>

<div class="text-justify mt-3 col-lg-12">
