<h1 class="mb-5">Politique de Confidentialité</h1> 

<div class="text-justify mt-3 col-lg-12">
	<strong>Quel type de renseignements personnels recueillons-nous ?</strong>
	<p>Nous recueillons les renseignements que vous fournissez volontairement. Ces informations sont recueillies, notamment lorsque vous vous inscrivez sur le site ou que vous effectuez une réservation en ligne.
