		<br>
		Nous faisons notre possible pour nous assurer que vos données personnelles soient valides, à jour et complètes afin d'en permettre l'utilisation pertinente. Selon les dispositions de la loi française sur la protection des données à caractère personnel, vous avez le droit d'accéder, d'actualiser, de corriger ou de supprimer vos données personnelles stockées par Cnam Location. Vous pouvez corriger des erreurs factuelles en nous les communiquant. Pour toute information concernant l'exercice de ces droits, vous trouverez l'adresse de notre siège social dans la rubrique ‘'Mentions légales'' sur notre site Web. 
		<br>
		Pour protéger votre confidentialité et votre sécurité, nous prendrons les mesures nécessaires afin de vérifier votre identité avant d'autoriser ou de réaliser lesdites corrections. 
