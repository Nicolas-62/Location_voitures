
<h1 class="mb-5">Des questions ?</h1>

<div class="text-justify col-lg-12">
	<strong>Quel âge faut-il avoir pour louer une voiture ?</strong>
	<p>
		Pour une location de voiture, l’âge minimum est 21 ans, avec un permis valide depuis au moins 2 ans. Attention, pour certaines catégories de véhicules, notamment les gros utilitaires, on pourra vous demander d’avoir un permis valide depuis 5 ans. 
	</p>
</div>

<div class="text-justify mt-3 col-lg-12">
	<strong>Quels documents ai-je besoin pour louer un véhicule ?</strong>
	<p>
		Lors de la prise en charge du véhicule à l’agence, vous devez présenter votre permis de conduire valide, votre carte d’identité ou un passeport valide. Renseignez-vous sur votre destination si un permis international est nécessaire. 
	</p>
</div>

<div class="text-justify mt-3 col-lg-12">
	<strong>Où puis-je circuler avec une voiture de location ?</strong>
	<p>
		En effectuant votre réservation en France, vous avez la possibilité de passer certaines frontières. Le choix du véhicule peut restreindre la circulation dans certains pays. Pour de plus amples informations, veuillez demander conseil en agence.
	</p>
</div>

<div class="text-justify mt-3 col-lg-12">
	<strong>Qui peut conduire/ne pas conduire le véhicule ?</strong>
	<p>
		Seul le conducteur étant désigné dans le contrat de location peut conduire le véhicule, à condition de présenter un permis de conduire et une pièce d’identité en cours de validité.
		<br>
		N’est pas autorisée à conduire toute personne non expressément désignée et/ou identifiée dans le Contrat de location. De même, sont exclues les personnes qui ne peuvent présenter les documents officiels listés précédemment.
	</p>
</div>

<div class="text-justify mt-3 col-lg-12">
	<strong>Comment puis-je payer une location de véhicule ?</strong>
	<p>Moyens de paiement acceptés par Cnam Car :</p>
	<ul>
		<li>Espèces</li>
		<li>Cartes de crédit</li>
		<li>Bon de location</li>
	</ul>
</div>

<div class="text-justify mt-3 col-lg-12">
	<strong>Quel type de véhicule peut être loué et à quelles fins ?</strong>
	<p>
		Vous pouvez louer soit un véhicule de tourisme, soit un véhicule utilitaire et vous devez conduire le véhicule conformément à l’usage prévu, à savoir :
	</p>
	<ul>
		<li>Les véhicules de tourisme sont destinés au transport d’un nombre variable de personnes (selon le nombre autorisé figurant sur la carte grise du véhicule) ;</li>
		<li>Les véhicules utilitaires peuvent être utilisés pour le transport de marchandises dans la limite de poids indiquée sur la carte grise du véhicule.</li>
	</ul>
	<p>	
		Cnam Location n’assure pas les marchandises transportées dans les véhicules, ni les effets personnels. De même, Cnam Location ne saurait être tenue pour responsable de toute perte de chance et perte d’exploitation intervenant dans le cadre de l’exécution du contrat de location.
	</p>
</div>


<div class="text-justify mt-3 col-lg-12">
	<strong>Dois-je faire le plein d'essence avant de rendre le véhicule ?</strong>
	<p>
		Cnam Location vous fournit un véhicule avec le plein de carburant. Vous devez donc refaire le plein avant de rendre le véhicule en agence. Dans le cas contraire, un supplément pourra être appliqué.
	</p>
</div>

<div class="text-justify mt-3 col-lg-12">
	<strong>Peut-on prendre en charge un véhicule dans une agence et le rendre dans une agence différente ?</strong>
	<p>
		Oui ! Vous avez le choix d’effectuer une location en aller-simple entre deux agences différentes. Pour cela, il suffit de sélectionner une agence de prise en charge (départ) et celle de restitution (arrivée).
	</p>
</div>

